const storyRoot = document.querySelector("#story");
const chapterNav = document.querySelector("#chapterNav");
const sourceList = document.querySelector("#sourceList");
const progressBar = document.querySelector("#progressBar");
const playButton = document.querySelector("#playTimeline");
const toggleVoiceButton = document.querySelector("#toggleVoice");

let autoPlayTimer = null;
let autoPlayIndex = 0;

const uniqueSources = new Map();

function mediaMarkup(mediaItem) {
  if (mediaItem.type === "image") {
    return `
      <figure>
        <img src="${mediaItem.src}" alt="${mediaItem.caption}" loading="lazy" />
        <figcaption>${mediaItem.caption}</figcaption>
      </figure>`;
  }

  if (mediaItem.type === "embed") {
    return `
      <figure>
        <iframe
          src="${mediaItem.src}"
          title="${mediaItem.caption}"
          loading="lazy"
          allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture"
          allowfullscreen
        ></iframe>
        <figcaption>${mediaItem.caption}</figcaption>
      </figure>`;
  }

  return "";
}

function createScene(scene, idx) {
  const article = document.createElement("article");
  article.className = "scene";
  article.id = `scene-${idx + 1}`;
  article.dataset.index = String(idx);

  const sources = scene.sources
    .map((source) => {
      uniqueSources.set(source.label, source.url);
      if (source.url === "#") {
        return `<li><span>${source.label}</span></li>`;
      }

      return `<li><a href="${source.url}" target="_blank" rel="noopener noreferrer">${source.label}</a></li>`;
    })
    .join("");

  const mediaHtml = scene.media.length
    ? `<div class="media-grid">${scene.media.map(mediaMarkup).join("")}</div>`
    : "";

  article.innerHTML = `
    <section class="scene-meta">
      <p class="period">${scene.period}</p>
      <h2>${scene.title}</h2>
      <p class="tag">${scene.beat}</p>
    </section>
    <section class="scene-content">
      <p>${scene.narrative}</p>
      <p class="voice">Sprechertext: ${scene.voice}</p>
      <p><strong>Overtourism-Bruecke:</strong> ${scene.overtourism}</p>
      ${mediaHtml}
      <ul class="sources">${sources}</ul>
    </section>
  `;

  return article;
}

function renderChapters() {
  storyData.forEach((scene, idx) => {
    const chapterButton = document.createElement("button");
    chapterButton.type = "button";
    chapterButton.textContent = `${idx + 1}. ${scene.period}`;
    chapterButton.addEventListener("click", () => goToScene(idx, true));
    chapterNav.appendChild(chapterButton);
  });
}

function renderStory() {
  storyData.forEach((scene, idx) => {
    storyRoot.appendChild(createScene(scene, idx));
  });
}

function renderSourceMatrix() {
  uniqueSources.forEach((url, label) => {
    const li = document.createElement("li");
    if (url === "#") {
      li.textContent = `${label} (lokale Datei: /Users/patrickfischer/Desktop/JakobWiedmer_Flut.pdf)`;
    } else if (url.startsWith("./")) {
      li.innerHTML = `${label}: <a href="${url}">${url}</a>`;
    } else {
      li.innerHTML = `<a href="${url}" target="_blank" rel="noopener noreferrer">${label}</a>`;
    }
    sourceList.appendChild(li);
  });
}

function setActiveChapter(index) {
  const buttons = chapterNav.querySelectorAll("button");
  buttons.forEach((button, idx) => {
    button.classList.toggle("active", idx === index);
  });
}

function setProgress(index) {
  const pct = ((index + 1) / storyData.length) * 100;
  progressBar.style.width = `${pct}%`;
}

function goToScene(index, userTriggered = false) {
  const target = document.querySelector(`#scene-${index + 1}`);
  if (!target) {
    return;
  }

  target.scrollIntoView({
    behavior: userTriggered ? "smooth" : "auto",
    block: "center"
  });
  setActiveChapter(index);
  setProgress(index);
}

function startAutoPlay() {
  if (autoPlayTimer) {
    clearInterval(autoPlayTimer);
    autoPlayTimer = null;
    playButton.textContent = "Zeitraffer Starten";
    return;
  }

  autoPlayIndex = 0;
  goToScene(autoPlayIndex, true);
  playButton.textContent = "Zeitraffer Stoppen";

  autoPlayTimer = setInterval(() => {
    autoPlayIndex += 1;
    if (autoPlayIndex >= storyData.length) {
      clearInterval(autoPlayTimer);
      autoPlayTimer = null;
      playButton.textContent = "Zeitraffer Starten";
      return;
    }
    goToScene(autoPlayIndex, true);
  }, 7000);
}

function toggleVoice() {
  const shouldHide = !document.body.classList.contains("hide-voice");
  document.body.classList.toggle("hide-voice", shouldHide);
  toggleVoiceButton.setAttribute("aria-pressed", String(!shouldHide));
  toggleVoiceButton.textContent = shouldHide
    ? "Sprechertext Einblenden"
    : "Sprechertext Ausblenden";
}

function revealOnScroll() {
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) {
          return;
        }

        const index = Number(entry.target.dataset.index);
        entry.target.classList.add("revealed");
        setActiveChapter(index);
        setProgress(index);
      });
    },
    { threshold: 0.33 }
  );

  document.querySelectorAll(".scene").forEach((scene) => observer.observe(scene));
}

renderChapters();
renderStory();
renderSourceMatrix();
revealOnScroll();
setActiveChapter(0);
setProgress(0);

playButton.addEventListener("click", startAutoPlay);
toggleVoiceButton.addEventListener("click", toggleVoice);
